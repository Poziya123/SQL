INCREMENT BY 1;

CREATE SEQUENCE ORDER_ID_SEQ START WITH 1001
INCREMENT BY 1;

CREATE SEQUENCE PAYMENT_ID_SEQ START WITH 1001
INCREMENT BY 1;

