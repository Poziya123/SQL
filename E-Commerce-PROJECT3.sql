DROP TABLE IF EXISTS PAYMENT CASCADE;
DROP TABLE IF EXISTS ORDER_DETAILS CASCADE;
DROP TABLE IF EXISTS ORDERS CASCADE;
DROP TABLE IF EXISTS PRODUCT CASCADE;
DROP TABLE IF EXISTS CUSTOMER CASCADE;
DROP TABLE IF EXISTS CATEGORY CASCADE;

CREATE TABLE CATEGORY (CATEGORY_ID INT PRIMARY KEY,CATEGORY_NAME VARCHAR(100) NOT NULL UNIQUE);

CREATE TABLE CUSTOMER (CUSTOMER_ID INT  PRIMARY KEY,CUSTOMER_NAME VARCHAR(100) NOT NULL,EMAIL VARCHAR(100) UNIQUE,
                       MOBILE_NO VARCHAR(15) UNIQUE,GENDER VARCHAR(10) CHECK (GENDER IN ('Male', 'Female', 'Other')),
					   REGISTRATION_DATE DATE,CITY VARCHAR(50));

CREATE TABLE PRODUCT(PRODUCT_ID INT PRIMARY KEY,PRODUCT_NAME VARCHAR(100) NOT NULL,
             CATEGORY_ID INT,FOREIGN KEY (CATEGORY_ID)REFERENCES CATEGORY(CATEGORY_ID),BRAND_NAME VARCHAR(100),
            PRICE DECIMAL(10,2) CHECK (PRICE > 0),STOCK_QUANTITY INT CHECK (STOCK_QUANTITY >= 0));

CREATE TABLE ORDERS(ORDER_ID INT PRIMARY KEY,CUSTOMER_ID INT,FOREIGN KEY (CUSTOMER_ID)REFERENCES CUSTOMER(CUSTOMER_ID),
             ORDER_DATE DATE,ORDER_STATUS VARCHAR(20)CHECK (ORDER_STATUS IN ('Placed','Packed','Shipped','Delivered','Cancelled')),
             TOTAL_AMOUNT DECIMAL(10,2)CHECK (TOTAL_AMOUNT > 0));

CREATE TABLE ORDER_DETAILS(ORDER_DETAIL_ID INT PRIMARY KEY,ORDER_ID INT,FOREIGN KEY (ORDER_ID)REFERENCES ORDERS(ORDER_ID),
             PRODUCT_ID INT,FOREIGN KEY (PRODUCT_ID)REFERENCES PRODUCT(PRODUCT_ID),QUANTITY INT CHECK (QUANTITY > 0),
             UNIT_PRICE DECIMAL(10,2) CHECK (UNIT_PRICE > 0));

CREATE TABLE PAYMENT(PAYMENT_ID INT PRIMARY KEY,ORDER_ID INT,FOREIGN KEY (ORDER_ID)REFERENCES ORDERS(ORDER_ID),
             PAYMENT_DATE DATE,PAYMENT_MODE VARCHAR(20)CHECK (PAYMENT_MODE IN ('UPI','Card','Net Banking','Cash On Delivery')),
             PAYMENT_AMOUNT DECIMAL(10,2)CHECK (PAYMENT_AMOUNT > 0),
             PAYMENT_STATUS VARCHAR(20)CHECK (PAYMENT_STATUS IN ('Success','Pending','Failed')));

CREATE TABLE SHIPPING(SHIPMENT_ID INT PRIMARY KEY,ORDER_ID INT,FOREIGN KEY (ORDER_ID)REFERENCES ORDERS(ORDER_ID),
             COURIER_PARTNER VARCHAR(100),SHIPMENT_DATE DATE,DELIVERY_DATE DATE,
             SHIPMENT_STATUS VARCHAR(20)CHECK (SHIPMENT_STATUS IN ('Packed','In Transit','Delivered','Returned')));

CREATE TABLE REVIEW(REVIEW_ID INT PRIMARY KEY,CUSTOMER_ID INT,FOREIGN KEY (PRODUCT_ID)REFERENCES PRODUCT(PRODUCT_ID),
             PRODUCT_ID INT,FOREIGN KEY (PRODUCT_ID)REFERENCES PRODUCT(PRODUCT_ID),
	         RATING INT CHECK (RATING BETWEEN 1 AND 5),REVIEW_DATE DATE);

