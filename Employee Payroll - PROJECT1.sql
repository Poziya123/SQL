DROP TABLE IF EXISTS EMPLOYEE CASCADE;
DROP TABLE IF EXISTS DESIGNATION CASCADE;
DROP TABLE IF EXISTS DEPARTMENT CASCADE;
CREATE TABLE DEPARTMENT(DEPT_ID INT PRIMARY KEY, DEPT_NAME VARCHAR(50) NOT NULL UNIQUE, LOCATION VARCHAR(50));

CREATE TABLE DESIGNATION(DESIG_ID INT PRIMARY KEY, DESIG_NAME VARCHAR(50) UNIQUE, MIN_SALARY INT CHECK (MIN_SALARY > 0),
                           MAX_SALARY INT CHECK (MAX_SALARY > MIN_SALARY));

CREATE TABLE EMPLOYEE(EMP_ID INT PRIMARY KEY,EMP_NAME VARCHAR(50) NOT NULL,EMAIL VARCHAR(100) UNIQUE,MOBILE_NO VARCHAR(15) UNIQUE,
    GENDER VARCHAR(10)CHECK (GENDER IN ('Male','Female','Other')),DOJ DATE,SALARY INT CHECK (SALARY > 10000),
	DEPT_ID INT,FOREIGN KEY (DEPT_ID)REFERENCES DEPARTMENT(DEPT_ID),
    DESIG_ID INT,FOREIGN KEY (DESIG_ID)REFERENCES DESIGNATION(DESIG_ID),
     MANAGER_ID INT,FOREIGN KEY (MANAGER_ID)REFERENCES EMPLOYEE(EMP_ID));

CREATE TABLE ATTENDANCE(ATT_ID INT PRIMARY KEY, EMP_ID INT,FOREIGN KEY (EMP_ID)REFERENCES EMPLOYEE(EMP_ID),
                        ATT_DATE DATE NOT NULL,ATT_STATUS VARCHAR(20)CHECK (ATT_STATUS IN ('Present','Absent','Leave')));

CREATE TABLE PAYROLL( PAYROLL_ID INT PRIMARY KEY,EMP_ID INT,FOREIGN KEY (EMP_ID)REFERENCES EMPLOYEE(EMP_ID),
    PAY_MONTH VARCHAR(20),BASIC_SALARY INT,BONUS INT CHECK (BONUS >= 0),DEDUCTION INT CHECK (DEDUCTION >= 0),
    NET_SALARY INT CHECK (NET_SALARY > 0));    

CREATE TABLE LEAVE_REQUEST(LEAVE_ID INT PRIMARY KEY,EMP_ID INT,FOREIGN KEY (EMP_ID)REFERENCES EMPLOYEE(EMP_ID),
        LEAVE_TYPE VARCHAR(30),FROM_DATE DATE,TO_DATE DATE,
         LEAVE_STATUS VARCHAR(20)CHECK (LEAVE_STATUS IN ('Pending','Approved','Rejected')));

CREATE TABLE PERFORMANCE(PERFOR_ID INT PRIMARY KEY,EMP_ID INT,FOREIGN KEY (EMP_ID)REFERENCES EMPLOYEE(EMP_ID),REVIEW_PERIOD VARCHAR(30),
    RATING INT CHECK (RATING BETWEEN 1 AND 5),REMARKS VARCHAR(200));

CREATE SEQUENCE EMP_SEQ START WITH 1001
INCREMENT BY 1;

CREATE SEQUENCE PAYROLL_SEQ START WITH 1001
INCREMENT BY 1;

CREATE SEQUENCE ATT_SEQ START WITH 1001
INCREMENT BY 1;

CREATE SEQUENCE LEAVE_SEQ START WITH 1001
INCREMENT BY 1;

