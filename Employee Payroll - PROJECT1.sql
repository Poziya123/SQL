CREATE VIEW ATTENDANCE_SUMMARY_VIEW AS SELECT EMP_NAME,
       COUNT(CASE WHEN ATT_STATUS = 'Present' THEN 1 END) AS PRESENT_DAYS,
       COUNT(CASE WHEN ATT_STATUS = 'Absent' THEN 1 END) AS ABSENT_DAYS,
       COUNT(CASE WHEN ATT_STATUS = 'Leave' THEN 1 END) AS LEAVE_DAYS FROM EMPLOYEE
       JOIN ATTENDANCE ON EMPLOYEE.EMP_ID = ATTENDANCE.EMP_ID GROUP BY EMP_NAME;
SELECT * FROM ATTENDANCE_SUMMARY_VIEW;

