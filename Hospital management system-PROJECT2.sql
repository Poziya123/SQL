CREATE VIEW HOSPITAL_REVENUE_REPORT AS SELECT DP.DEPT_NAME,
       SUM(B.TOTAL_AMOUNT) AS REVENUE,
       COUNT(T.TREATMENT_ID) AS NUMBER_OF_TREATMENTS FROM DEPARTMENT DP
       JOIN DOCTOR D ON DP.DEPT_ID = D.DEPT_ID
       JOIN TREATMENT T ON D.DOC_ID = T.DOC_ID
       JOIN BILLING B ON T.TREATMENT_ID = B.TREATMENT_ID GROUP BY DP.DEPT_NAME;
SELECT * FROM HOSPITAL_REVENUE_REPORT;

