CREATE VIEW PATIENT_VISIT_REPORT AS SELECT P.PAT_NAME,D.DOC_NAME,DP.DEPT_NAME,A.APPOINTMENT_DATE FROM APPOINTMENT A
           JOIN PATIENT P ON A.PAT_ID = P.PAT_ID
           JOIN DOCTOR D ON A.DOC_ID = D.DOC_ID
           JOIN DEPARTMENT DP ON D.DEPT_ID = DP.DEPT_ID;
