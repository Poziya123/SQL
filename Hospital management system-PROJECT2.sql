DROP TABLE IF EXISTS PAYMENT CASCADE;
DROP TABLE IF EXISTS BILLING CASCADE;
DROP TABLE IF EXISTS TREATMENT CASCADE;
DROP TABLE IF EXISTS ADMISSION CASCADE;
DROP TABLE IF EXISTS APPOINTMENT CASCADE;
DROP TABLE IF EXISTS PATIENT CASCADE;
DROP TABLE IF EXISTS DOCTOR CASCADE;
DROP TABLE IF EXISTS DEPARTMENT CASCADE;
CREATE TABLE DEPARTMENT(DEPT_ID INT PRIMARY KEY, DEPT_NAME VARCHAR(50) NOT NULL UNIQUE, LOCATION VARCHAR(50));

CREATE TABLE DOCTOR(DOC_ID INT PRIMARY KEY,DOC_NAME VARCHAR(50) NOT NULL, EMAIL VARCHAR(50) UNIQUE,MOBILE_NO VARCHAR(10) UNIQUE,
                     SPECIALIZATION VARCHAR(50),DEPT_ID INT, JOINING_DATE DATE,CONSULTATION_FEE INT CHECK (CONSULTATION_FEE > 0),
					  FOREIGN KEY (DEPT_ID) REFERENCES DEPARTMENT(DEPT_ID));
					  
CREATE TABLE PATIENT(PAT_ID INT PRIMARY KEY, PAT_NAME VARCHAR(50) NOT NULL,GENDER VARCHAR(10)CHECK (GENDER IN ('Male','Female','Other')),
                     DOJ DATE,MOBILE_NO VARCHAR(10) UNIQUE,EMAIL VARCHAR(50) UNIQUE,BLOOD_GROUP VARCHAR(5),REGISTRATION_DATE DATE NOT NULL);
					 
CREATE TABLE APPOINTMENT(APPOINTMENT_ID INT PRIMARY KEY,PAT_ID INT,FOREIGN KEY (PAT_ID)REFERENCES PATIENT(PAT_ID),
                         DOC_ID INT,FOREIGN KEY (DOC_ID)REFERENCES DOCTOR(DOC_ID),APPOINTMENT_DATE DATE,
                         APPOINTMENT_STATUS VARCHAR(20)CHECK (APPOINTMENT_STATUS IN ('Booked', 'Completed', 'Cancelled')));
						 
CREATE TABLE ADMISSION(ADMISSION_ID INT PRIMARY KEY,PAT_ID INT,FOREIGN KEY (PAT_ID)REFERENCES PATIENT(PAT_ID),
                        ROOM_NO VARCHAR(10),ADMISSION_DATE DATE NOT NULL,DISCHARGE_DATE DATE);

CREATE TABLE TREATMENT(TREATMENT_ID INT PRIMARY KEY,PAT_ID INT,FOREIGN KEY (PAT_ID)REFERENCES PATIENT(PAT_ID),
                         DOC_ID INT,FOREIGN KEY (DOC_ID)REFERENCES DOCTOR(DOC_ID),TREATMENT_NAME VARCHAR(100),
                         TREATMENT_COST INT CHECK (TREATMENT_COST > 0),TREATMENT_DATE DATE);

CREATE TABLE BILLING(BILL_ID INT PRIMARY KEY,PAT_ID INT,FOREIGN KEY (PAT_ID)REFERENCES PATIENT(PAT_ID),
                     TREATMENT_ID INT,FOREIGN KEY (TREATMENT_ID)REFERENCES TREATMENT(TREATMENT_ID),TOTAL_AMOUNT INT CHECK (TOTAL_AMOUNT > 0),
                     PAYMENT_STATUS VARCHAR(20)CHECK (PAYMENT_STATUS IN ('Paid', 'Pending')));

CREATE TABLE PAYMENT(PAYMENT_ID INT PRIMARY KEY,
                     BILL_ID INT,FOREIGN KEY (BILL_ID)REFERENCES BILLING(BILL_ID),PAYMENT_DATE DATE,
                     PAYMENT_MODE VARCHAR(20)CHECK (PAYMENT_MODE IN ('Cash', 'Card', 'UPI', 'Net Banking')),
                     AMOUNT_PAID INT CHECK (AMOUNT_PAID > 0));

